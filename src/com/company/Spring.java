package com.company;

public class Spring {
    private double x0, y1, z2, x, y, z, k;

    public Spring(double x0, double y1, double z2, double x, double y, double z, double k) {
        this.x0 = x0;
        this.y1 = y1;
        this.z2 = z2;
        this.x = x;
        this.y = y;
        this.z = z;
        this.k = k;
    }

    // test

    public double getX0() {
        return x0;
    }

    public double getY1() {
        return y1;
    }

    public double getZ2() {
        return z2;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }

    public double getZ() {
        return z;
    }

    public double getK() {
        return k;
    }
}
